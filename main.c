//    <trkseg>
//      <trkpt lat="44.3901710" lon="5.1954480">
//        <ele>323.9</ele>
//        <time>2021-02-19T22:54:09Z</time>
//        <extensions>
//          <power>25</power>
//          <gpxtpx:TrackPointExtension>
